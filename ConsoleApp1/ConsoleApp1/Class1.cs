using System;


namespace ConsoleApp1
{
    public class IfClass
    {
        static void Main(string[] args)
        {
            int i = 10, j = 20;

            if (i < j)
            {
                Console.WriteLine("i is less than j");
            }

            if (i > j)
            {
                Console.WriteLine("i is greater than j");
            }
        }

    }

}
